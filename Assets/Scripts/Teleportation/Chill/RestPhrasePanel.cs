using UnityEngine;
using TMPro;

public class RestPhrasePanel : MonoBehaviour
{
    public GameObject panel; // Твоя вторая панель для фразы (перетащи в инспекторе)
    public TMP_Text phraseText; // Текстовое поле внутри панели

    [TextArea]
    public string[] phrases = {
        "Ты проводишь пальцем по корешкам старых книг. Шёпот забытых историй наполняет комнату, а страницы манят уйти от тревог. Позволить себе отдых среди мудрости веков?",
        "Тусклый свет льётся на пыльную полку. Здесь царит спокойствие, и шелест страниц кажется самой надёжной защитой. Остановиться и погрузиться в этот мир — мудрое решение.",
        "Вокруг пахнет старыми томами и тайнами, которые они хранят. Сердце замедляет бег, а мысли становятся яснее. Может, стоит ненадолго забыть о тревогах и позволить себе немного покоя?",
        "Пока за дверями шумит опасный мир, здесь, у книжной полки, время замирает. Возможно, сейчас — лучший момент, чтобы перевести дух и обрести силы для нового пути.",
        "Ты подходишь к полке, чувствуя лёгкий холодок древней магии. Кажется, книги охраняют твой покой. Сделать передышку в этом укромном уголке?"
    };

    public void ShowPanel()
    {
        if (panel != null)
            panel.SetActive(true);

        if (phraseText != null && phrases.Length > 0)
        {
            int idx = Random.Range(0, phrases.Length);
            phraseText.text = phrases[idx];
        }
    }

    public void HidePanel()
    {
        if (panel != null)
            panel.SetActive(false);
    }
}
